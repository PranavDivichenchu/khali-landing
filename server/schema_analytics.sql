-- Table to store user interactions for analytics and recommendations
CREATE TABLE IF NOT EXISTS user_interactions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL,
  news_id UUID NOT NULL,
  interaction_type TEXT NOT NULL,
  metadata JSONB DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- Index for fast user history lookup
CREATE INDEX IF NOT EXISTS idx_user_interactions_user_id ON user_interactions(user_id);
CREATE INDEX IF NOT EXISTS idx_user_interactions_created_at ON user_interactions(created_at DESC);

-- Table to store explicit user preferences (Weighted categories)
CREATE TABLE IF NOT EXISTS user_preferences (
  user_id UUID NOT NULL,
  category TEXT NOT NULL,
  weight INTEGER DEFAULT 0,
  updated_at TIMESTAMPTZ DEFAULT now(),
  PRIMARY KEY (user_id, category)
);
